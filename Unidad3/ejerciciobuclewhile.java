/**
 * ejerciciobuclewhile
 *Escribe un programa que muestre por consola los numero del 1 al 50 utiliznado un bucle while 
 *Inicializa el numero al valor = 1
 *Ten cuidado de no entrar en un bucle infinito
 */
public class ejerciciobuclewhile {

    public static void main(String[] args) {

        byte x=0;
        while (x<50){
            x++;
            System.out.println(x);
        }
        
    }
}